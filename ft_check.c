#include "ft_printf.h"

int	ft_check(char c)
{
	if ((c == 'c') || (c == '%') || (c == 'd')
		|| (c == 'x') || (c == 'X') || (c == 's')
		|| (c == 'u') || (c == 'i') || (c == 'p'))
		return (1);
	return (0);
}
